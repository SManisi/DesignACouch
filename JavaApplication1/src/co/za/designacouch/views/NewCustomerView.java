    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        CustomerDo customerDo = new CustomerDo();
        customerDo.setCustomerNumber(PROPERTIES);
        customerDo.setCustomerType((String)customerTypeCombo.getSelectedItem());
        customerDo.setEmail(emailField.getText());
        customerDo.setMiddleName(middleNameField.getText());
        customerDo.setName(nameField.getText());
        customerDo.setPostalAddress(postalField.getText());
        customerDo.setPostalAddressPostCode(postCodeField.getText());
        customerDo.setResidentialAddress(residentialField.getText());
        customerDo.setResidentialPostCode(residentialPostCodeField.getText());
        customerDo.setSurname(surnameField.getText());
        customerDo.setTelephoneCell(telephoneCellField.getText());
        customerDo.setTelephoneWork(telephoneWorkField.getText());
        customerDo.setTitle((String)(titleCombo.getSelectedItem()));

        CustomerService customerService = new CustomerServiceImpl();
        customerService.recordCustomerDo(customerDo);
        
        JOptionPane.showMessageDialog(jMenu1, "Customer Succefully created!");
    }//GEN-LAST:event_saveButtonActionPerformed

