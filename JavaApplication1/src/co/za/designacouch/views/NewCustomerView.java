    private void customerTypeComboActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_customerTypeComboActionPerformed
        String customerType = (String) customerTypeCombo.getSelectedItem();
        if (customerType.equals("Company")) {
            titleCombo.setEnabled(false);
            titleCombo.setSelectedItem("");
        } else {
            titleCombo.setEnabled(true);
        }
    }//GEN-LAST:event_customerTypeComboActionPerformed

    private void sameCheckBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_sameCheckBoxActionPerformed
        if (sameCheckBox.isSelected()) {
            postalField.setText(residentialField.getText());
            postCodeField.setText(residentialPostCodeField.getText());
            postalField.setEnabled(false);
            postCodeField.setEnabled(false);
        } else {
            postalField.setText("");
            postCodeField.setText("");
            postCodeField.setEnabled(true);
            postalField.setEnabled(true);
        }
    }//GEN-LAST:event_sameCheckBoxActionPerformed

